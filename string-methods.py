name = "hunter"

#length if the string
print(len(name))
#find index of the string
print(name.find("t"))
#capitalize
print(name.capitalize())
#uppercase
print(name.upper())
#lowercase
print(name.lower())
#isDigit
print(name.isdigit())
#are these alphabetical characters?
print(name.isalpha())
#count how many characters in the string
print(name.count("u"))
#replace character in a string
print(name.replace("h", "b"))
#display string n times
print(name*3)